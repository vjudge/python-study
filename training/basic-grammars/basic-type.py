# 整数，浮点数，复数
print('------ 整数，浮点数，复数 ------')
a = 123
b = 3.14
print('a = ', int(b))
print('b = ', float(a))
print('complex(a) = ', complex(a))
print('complex(a, b) = ', complex(a, b))

# 字符串
print('------ 字符串 ------')
str1 = 'hello world!'
print('str1: ', str1)
print('str1[0]: ', str1[0])
print('str1[6:11]: ', str1[6:11])
print('str1[6:11]: ', str1[6:11])

print('字符替换：', str1.replace('world', 'python'))
print('首字符大写：', str1.title())
print('连接字符串：', '-'.join(['111', '222', '333']))


# 赋值
a, b = {'c': 1, 'd': 2}
print('a: ', a)
# a:  c
print('b: ', b)
# b:  d

a, b = 1, 2
print('a: ', a)
# a:  1
print('b: ', b)
# b:  2





